#include <iostream>
using namespace std;
int main() {

    // Operator Aritmatika
    int a = 10, b = 20;
    cout << "Operator Aritmatika: " << endl;
    cout << "a + b = " << a + b << endl; // Pemjumlahan
    cout << "a - b = " << a - b << endl; // Pengurangan
    cout << "a * b = " << a * b << endl; // Perkalian
    cout << "a / b = " << a / b << endl; // Pembagian
    cout << "a % b = " << a % b << endl; // Modulus

    // Operator Penugasan
    int c = 5;

    return 0;
    }
